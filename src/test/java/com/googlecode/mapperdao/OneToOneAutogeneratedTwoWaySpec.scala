package com.googlecode.mapperdao

import org.specs2.mutable.SpecificationWithJUnit
import com.googlecode.mapperdao.jdbc.Setup

/**
 * @author kostantinos.kougios
 *
 * 5 Sep 2011
 */
class OneToOneAutogeneratedTwoWaySpec extends SpecificationWithJUnit {
	import OneToOneAutogeneratedTwoWaySpec._
	val (jdbc, mapperDao) = Setup.setupMapperDao(TypeRegistry(ProductEntity, InventoryEntity))

	import mapperDao._

	"update to null" in {
		createTables
		val product = Product(Inventory(null, 5))
		product.inventory.product = product
		val inserted = insert(ProductEntity, product)
		inserted.inventory = null
		val updated = update(ProductEntity, inserted)
		updated.inventory must_== null
		select(ProductEntity, inserted.id).get must_== updated
	}

	"CRUD mutable" in {
		createTables
		val product = Product(Inventory(null, 5))
		product.inventory.product = product
		val inserted = insert(ProductEntity, product)
		inserted.inventory.stock = 8
		val updated = update(ProductEntity, inserted)
		updated must_== inserted
		val selected = select(ProductEntity, updated.id).get
		selected must_== updated

		delete(ProductEntity, selected)
		select(ProductEntity, selected.id) must beNone
	}

	"insert & select mutable" in {
		createTables
		val product = Product(Inventory(null, 5))
		product.inventory.product = product
		val inserted = insert(ProductEntity, product)
		inserted.inventory.stock must_== product.inventory.stock
		val selected = select(ProductEntity, inserted.id).get
		selected must_== inserted
	}

	"from null to value" in {
		createTables
		val product = Product(null)
		val inserted = insert(ProductEntity, product)
		inserted.inventory = Inventory(inserted, 5)
		val updated = update(ProductEntity, inserted)
		updated.inventory must_== inserted.inventory
		select(ProductEntity, inserted.id).get must_== updated

		delete(ProductEntity, updated)
		select(ProductEntity, updated.id) must beNone
	}

	def createTables =
		{
			Setup.dropAllTables(jdbc)
			Setup.database match {
				case "postgresql" =>
					Setup.createMySeq(jdbc)
					jdbc.update("""
				create table Product (
					id int not null,
					primary key (id)
				)
			""")
					jdbc.update("""
				create table Inventory (
					product_id int not null,
					stock int not null,
					primary key (product_id),
					foreign key (product_id) references Product(id) on delete cascade
				)
			""")
				case "oracle" =>
					Setup.createMySeq(jdbc)
					jdbc.update("""
				create table Product (
					id int not null,
					primary key (id)
				)
			""")
					jdbc.update("""
				create table Inventory (
					product_id int not null,
					stock int not null,
					primary key (product_id),
					foreign key (product_id) references Product(id) on delete cascade
				)
			""")
					Setup.oracleTrigger(jdbc, "Product")
				case "mysql" =>
					jdbc.update("""
				create table Product (
					id serial not null,
					primary key (id)
				)
			""")
					jdbc.update("""
				create table Inventory (
					product_id int not null,
					stock int not null,
					primary key (product_id),
					foreign key (product_id) references Product(id) on delete cascade
				)
			""")
			}
		}

}

object OneToOneAutogeneratedTwoWaySpec {
	case class Inventory(var product: Product, var stock: Int) {
		override def hashCode = stock
		override def equals(v: Any) = v match {
			case i: Inventory => i.stock == stock && ((i.product == null && product == null) || (i.product != null && product != null && i.product == product))
			case _ => false
		}
		override def toString = "Inventory(%d, productId:%s)".format(stock, product match {
			case p: Product with IntId => p.id
			case p: Product => "Product(not persisted)"
			case null => null
		})
	}
	case class Product(var inventory: Inventory) {
		override def equals(v: Any) = v match {
			case p: Product with IntId =>
				this match {
					case tp: Product with IntId => tp.id == p.id
					case _ => false
				}
			case _ => false
		}
	}

	object InventoryEntity extends SimpleEntity[Inventory](classOf[Inventory]) {
		val product = oneToOne(classOf[Product], _.product)
		val stock = int("stock", _.stock)

		val constructor = (m: ValuesMap) => new Inventory(m(product), m(stock)) with Persisted {
			val valuesMap = m
		}
	}

	val sequence = Setup.database match {
		case "postgresql" | "oracle" => "myseq"
		case _ => null
	}
	object ProductEntity extends Entity[IntId, Product](classOf[Product]) {
		val id = if (sequence == null) intAutoGeneratedPK("id", _.id) else intAutoGeneratedPK("id", sequence, _.id)
		val inventory = oneToOneReverse(classOf[Inventory], _.inventory)

		val constructor = (m: ValuesMap) => new Product(m(inventory)) with IntId with Persisted {
			val valuesMap = m
			val id = m(ProductEntity.id)
		}
	}

}